#include "../ft_printf.h"

int	ft_pn_di(int n)
{
	unsigned int	x;

	x = 0;
	if (n < 0)
	{
		write(1, "-", 1);
		x = -n;
	}
	else
		x = n;
	if (x > 9)
	{
		ft_pn_di(x / 10);
		ft_pn_di(x % 10);
	}
	else
	{
		x += '0';
		write(1, &x, 1);
	}
	return (ft_intlen(n));
}
